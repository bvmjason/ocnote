export interface FullArticle {
  id: string
  title: string
  description: string
  category: string
  order: number
  readTime: string
  content: string
  date?: string
  source?: string
  sourceUrl?: string
}

export async function loadNewsArticles(): Promise<FullArticle[]> {
  const articles: FullArticle[] = []
  
  for (const article of NEWS_LIST) {
    try {
      const response = await fetch(article.path)
      const content = await response.text()
      
      const frontmatterMatch = content.match(/^---\n([\s\S]*?)\n---\n([\s\S]*)$/)
      
      if (frontmatterMatch) {
        const frontmatter = frontmatterMatch[1]
        const markdownContent = frontmatterMatch[2]
        
        const metadata: Partial<FullArticle> = {}
        frontmatter.split('\n').forEach((line: string) => {
          const [key, ...valueParts] = line.split(':')
          if (key && valueParts.length) {
            const value = valueParts.join(':').trim().replace(/^["']|["']$/g, '')
            metadata[key.trim() as keyof FullArticle] = value as any
          }
        })
        
        articles.push({
          id: article.id,
          title: metadata.title || '',
          description: metadata.description || '',
          category: metadata.category || 'news',
          order: article.order,
          readTime: metadata.readTime || '5 分钟',
          date: metadata.date,
          source: metadata.source,
          sourceUrl: metadata.sourceUrl,
          content: markdownContent,
        })
      }
    } catch (error) {
      console.error(`Failed to load news article ${article.id}:`, error)
    }
  }
  
  return articles.sort((a, b) => a.order - b.order)
}

export async function loadNewsArticleById(id: string): Promise<FullArticle | null> {
  const articles = await loadNewsArticles()
  return articles.find(a => a.id === id) || null
}
